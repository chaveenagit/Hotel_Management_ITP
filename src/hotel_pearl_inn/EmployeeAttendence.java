    //Insert
    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        
        //get data from the fields
        SimpleDateFormat dateFormat  = new SimpleDateFormat("YYYY-MM-dd");
        String date = dateFormat.format(jDateChooserdate.getDate());
        
        String empName = jTextFieldemployeeName.getText();
        
        String status ="Present";
        
        if(jRadioButtonAbsent.isSelected()){
           status = "Absent";
        }
       
        String timeStatus = "On Time";
        
        if(jRadioButtonOutTime.isSelected()){
           timeStatus = "Out Time";
        }
        
        
       //not null
        if(date.trim().equals("")|| empName.trim().equals("")|| status.trim().equals("")|| timeStatus.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Required Fields", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }else{
            if(emp.addEmployeeAttendence(date,empName,status,timeStatus)){
            JOptionPane.showMessageDialog(rootPane, "Employee Attendance Added Successfully", "Add Attendance", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Attendance not Added", "Add attendance Error", JOptionPane.ERROR_MESSAGE);
            
        }
        }
    }//GEN-LAST:event_jButtonSubmitActionPerformed

