    private void jButtonaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonaddActionPerformed
         //get data from the fields
        String name= jTextFieldname.getText();
        String Address= jTextFieldaddress.getText();
        String email= jTextFieldemail.getText();
        String Tele= jTextFieldtele.getText();
        String Gender="Male";
        
        if(jRadioButtonfemale.isSelected()){
           Gender= "Female";
        }
        SimpleDateFormat dateFormat  = new SimpleDateFormat("YYYY-MM-dd");
        String DOB = dateFormat.format(jDateChooserdob.getDate());
        
        String exp= jTextFieldexde.getText();
        String Desg= jTextFielddes.getText();
        String Dep= jTextFielddep.getText();
        String Basicsal= jTextFieldbasicsal.getText();
        String EmpType="Permenent";
        
        if(jRadioButtoncon.isSelected()){
           EmpType= "Contract";
        }
        
        //validations
       Pattern p = Pattern.compile("^[0-9][0-9]{9}$");
       Matcher m = p.matcher(jTextFieldtele.getText());
       
       Pattern p2 = Pattern.compile("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$");
       Matcher m2 = p2.matcher(jTextFieldemail.getText());
        
       //not null
        if(name.trim().equals("")|| Address.trim().equals("")|| email.trim().equals("")|| Tele.trim().equals("") || exp.trim().equals("")|| Desg.trim().equals("")|| Basicsal.trim().equals("")|| Gender.trim().equals("")|| EmpType.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Required Fields", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }else{
            if(!m2.matches()){
                JOptionPane.showMessageDialog(null,"Invalid email");
        }else{
            if(!m.matches()){
                JOptionPane.showMessageDialog(null,"Invalid Mobile Number");
            }else{
            if(emp.addEmployee(name, Address, email, Tele,Gender,DOB ,exp, Desg, Dep, Basicsal,EmpType)){
            JOptionPane.showMessageDialog(rootPane, "New Employee Added Successfully", "Add Employee", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Employee not Added", "Add Employee Error", JOptionPane.ERROR_MESSAGE);
            
        }}}
        }
    }//GEN-LAST:event_jButtonaddActionPerformed

