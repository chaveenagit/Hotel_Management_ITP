    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       //get the jtable model
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        //get the selected raw indexes
        int rIndex = jTable1.getSelectedRow();
        
        //display data
        jTextFieldempid.setText(model.getValueAt(rIndex, 0).toString());
        jTextFieldname.setText(model.getValueAt(rIndex, 1).toString());
        jTextFieldaddress.setText(model.getValueAt(rIndex, 2).toString());
        jTextFieldemail.setText(model.getValueAt(rIndex, 3).toString());
        jTextFieldtele.setText(model.getValueAt(rIndex, 4).toString());
        jTextFieldexde.setText(model.getValueAt(rIndex, 7).toString());
        jTextFielddes.setText(model.getValueAt(rIndex, 8).toString());
        jTextFielddep.setText(model.getValueAt(rIndex, 9).toString());
        jTextFieldbasicsal.setText(model.getValueAt(rIndex, 10).toString());
        
         String Gender = model.getValueAt(rIndex, 5).toString();
        
        if(Gender.equals("Female")){
            jRadioButtonfemale.setSelected(true);
        }else if(Gender.equals("Male")){
            jRadioButtonmale.setSelected(true);
        }
        
        String EmpType = model.getValueAt(rIndex, 11).toString();
        
        if(EmpType.equals("Contract")){
            jRadioButtoncon.setSelected(true);
        }else if(EmpType.equals("Permenent")){
            jRadioButtonper.setSelected(true);
        }
        
        try{
        Date DOB =  new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rIndex, 6).toString());
        jDateChooserdob.setDate(DOB);
        }catch(Exception ex){
            Logger.getLogger(Employee_class.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }//GEN-LAST:event_jTable1MouseClicked

