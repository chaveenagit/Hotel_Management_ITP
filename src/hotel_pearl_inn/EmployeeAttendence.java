    //export
    private void jButtonExportTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExportTableActionPerformed
        
        String path="";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x=j.showSaveDialog(this);
        
        if(x==JFileChooser.APPROVE_OPTION)
        {
            path = j.getSelectedFile().getPath();
            
        }
        
        Document doc = new Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path+"employeeAttendance.pdf"));
            
            doc.open();
            PdfPTable tbl = new PdfPTable(4);
            
            //Adding header
            tbl.addCell("Date");
            tbl.addCell("Employee Name");
            tbl.addCell("Status");
            tbl.addCell("Time Status");
            
            for(int i = 0; i < jTableAttendance.getRowCount(); i++){
                
                String date = jTableAttendance.getValueAt(i, 0).toString();
                String name = jTableAttendance.getValueAt(i, 1).toString();
                String status = jTableAttendance.getValueAt(i, 2).toString();
                String tstatus = jTableAttendance.getValueAt(i, 3).toString();
                
                //store data into pdf table
                tbl.addCell(date);
                tbl.addCell(name);
                tbl.addCell(status);
                tbl.addCell(tstatus);
                
            }
            
            //add table to pdf file
            doc.add(tbl);
            
            JOptionPane.showMessageDialog(null, "Successfully Downloaded pdf file", "ALERT MESSAGE", JOptionPane.INFORMATION_MESSAGE);
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(EmployeeAttendence.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(EmployeeAttendence.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        doc.close();
        
    }//GEN-LAST:event_jButtonExportTableActionPerformed

